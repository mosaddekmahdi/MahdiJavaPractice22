package animal;

public class Cat {
	String color;
	int leg;
	boolean isCat;
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getLeg() {
		return leg;
	}
	public void setLeg(int leg) {
		this.leg = leg;
	}
	public boolean isCat() {
		return isCat;
	}
	public void setCat(boolean isCat) {
		this.isCat = isCat;
	}
	
	
	
	
}
